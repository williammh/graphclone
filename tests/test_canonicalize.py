# ---------------------------------------------------------------------------
# canonicalize_url – Prong 2A (cardinality-based slug collapsing)
# ---------------------------------------------------------------------------

class TestCanonicalizeUrlProng2A:
    """Cardinality-based collapsing for the last segment."""

    def _make_observed(self, netloc, parent_tuple, values):
        """Helper: build observed_last_segment_values dict."""
        return {(netloc, parent_tuple): set(values)}

    def _make_static(self, netloc, parent_tuple, values):
        """Helper: build observed_static_segments dict."""
        return {(netloc, parent_tuple): set(values)}

    def test_below_threshold_no_collapse(self):
        """Fewer than SLUG_CARDINALITY_THRESHOLD values → keep literal."""
        obs = self._make_observed("www.forexfactory.com", (), ["bobjones", "janedoe"])
        url = "https://www.forexfactory.com/bobjones"
        result = canonicalize_url(url, observed_last_segment_values=obs)
        assert result == "https://www.forexfactory.com/bobjones"

    def test_at_threshold_collapses_unseen(self):
        """Once cardinality >= threshold, new (unseen-as-static) values collapse."""
        threshold = config.SLUG_CARDINALITY_THRESHOLD
        users = [f"user{i}" for i in range(threshold)]
        obs = self._make_observed("www.forexfactory.com", (), users)
        # Static segments are the ones seen before threshold was hit
        static = self._make_static("www.forexfactory.com", (), users)

        # A brand-new username should collapse
        url = "https://www.forexfactory.com/newuser"
        result = canonicalize_url(url, obs, static)
        assert result == "https://www.forexfactory.com/:slug"

    def test_at_threshold_keeps_static(self):
        """Segments locked-in before threshold are never collapsed."""
        threshold = config.SLUG_CARDINALITY_THRESHOLD
        users = [f"user{i}" for i in range(threshold)]
        obs = self._make_observed("www.forexfactory.com", (), users)
        static = self._make_static("www.forexfactory.com", (), users)

        # "user0" was locked in as static → stays literal
        url = "https://www.forexfactory.com/user0"
        result = canonicalize_url(url, obs, static)
        assert result == "https://www.forexfactory.com/user0"


# ---------------------------------------------------------------------------
# ForexFactory-style dynamic user profiles  (/{username})
# ---------------------------------------------------------------------------

class TestForexFactoryUserProfiles:
    """forexfactory.com/bobjones, forexfactory.com/janedoe, etc.

    These are top-level dynamic segments that look like static routes to a
    naïve parser.  The system must eventually learn to collapse them.
    """

    NETLOC = "www.forexfactory.com"

    def _simulate_many_profiles(self, count):
        """Simulate observing `count` unique usernames at the root level."""
        names = [f"user{i}" for i in range(count)]
        obs = {(self.NETLOC, ()): set(names)}
        # First SLUG_CARDINALITY_THRESHOLD are locked as static
        locked = set(names[: config.SLUG_CARDINALITY_THRESHOLD])
        static = {(self.NETLOC, ()): locked}
        return obs, static

    def test_early_profiles_kept_literal(self):
        """Before enough profiles are seen, usernames stay literal."""
        obs, static = self._simulate_many_profiles(3)
        url = f"https://{self.NETLOC}/user1"
        result = canonicalize_url(url, obs, static)
        assert result == f"https://{self.NETLOC}/user1"

    def test_profiles_collapse_after_threshold(self):
        """After threshold, unseen usernames collapse to :slug."""
        obs, static = self._simulate_many_profiles(config.SLUG_CARDINALITY_THRESHOLD)
        url = f"https://{self.NETLOC}/brandnewuser"
        result = canonicalize_url(url, obs, static)
        assert result == f"https://{self.NETLOC}/:slug"

    def test_known_static_route_not_collapsed(self):
        """Real static routes (calendar, forum) stay even when cardinality is high."""
        obs, static = self._simulate_many_profiles(config.SLUG_CARDINALITY_THRESHOLD)
        # Manually add "calendar" to the static set (as the crawler would)
        static[(self.NETLOC, ())].add("calendar")

        url = f"https://{self.NETLOC}/calendar"
        result = canonicalize_url(url, obs, static)
        assert result == f"https://{self.NETLOC}/calendar"

    def test_nav_link_keeps_segment_static(self):
        """Prong 2B: a segment with nav_link_count >= 2 is immune to collapsing."""
        obs, static = self._simulate_many_profiles(config.SLUG_CARDINALITY_THRESHOLD)
        stats = {
            (self.NETLOC, "/forum"): SegmentStats(nav_link_count=3, in_link_count=10),
        }
        # "forum" not in static set, but nav signals save it
        url = f"https://{self.NETLOC}/forum"
        result = canonicalize_url(url, obs, static, stats)
        assert result == f"https://{self.NETLOC}/forum"


