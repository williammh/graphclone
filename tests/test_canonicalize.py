class TestIntegrationScenarios:
    """End-to-end scenarios mixing multiple prongs."""

    def test_forexfactory_thread_with_id_slug(self):
        """thread/1386286-trump → thread/:id-:slug (Prong 1 alone)."""
        url = "https://www.forexfactory.com/thread/1386286-trump-was-once"
        assert canonicalize_url(url) == "https://www.forexfactory.com/thread/:id-:slug"

    def test_forexfactory_attachment_numeric_id(self):
        url = "https://www.forexfactory.com/attachment/987654"
        assert canonicalize_url(url) == "https://www.forexfactory.com/attachment/:id"

    def test_ziprecruiter_job_posting_with_id(self):
        url = "https://www.ziprecruiter.com/jobs/walmart/12345678"
        assert canonicalize_url(url) == "https://www.ziprecruiter.com/jobs/walmart/:id"

    def test_struct_heuristic_plus_cardinality(self):
        """Numeric ID via Prong 1, username via Prong 2A."""
        netloc = "www.example.com"
        threshold = config.SLUG_CARDINALITY_THRESHOLD
        users = [f"u{i}" for i in range(threshold)]
        obs = {(netloc, ()): set(users)}
        static = {(netloc, ()): set(users)}

        url = f"https://{netloc}/newuser/post/99999"
        result = canonicalize_url(url, obs, static)
        # /newuser collapses (Prong 2A), /post stays, /99999 → :id (Prong 1)
        assert result == f"https://{netloc}/:slug/post/:id"

    def test_all_prongs_together(self):
        """Combine Prong 1, 2A, 2B, and 2C in one URL."""
        netloc = "www.complex.com"
        threshold = config.SLUG_CARDINALITY_THRESHOLD

        # Prong 2A data: position-0 has high cardinality
        obs = {(netloc, ()): set(f"cat{i}" for i in range(threshold))}
        static = {(netloc, ()): set(f"cat{i}" for i in range(threshold))}

        # Prong 2B: /docs has nav links so it's protected
        stats = {
            (netloc, "/docs"): SegmentStats(nav_link_count=4, in_link_count=20),
        }

        url = f"https://{netloc}/docs/12345"
        result = canonicalize_url(url, obs, static, stats)
        # "docs" stays (nav signal), "12345" → :id (Prong 1)
        assert result == f"https://{netloc}/docs/:id"

    def test_scheme_defaults_to_https(self):
        url = "https://example.com/page"
        result = canonicalize_url(url)
        assert result.startswith("https://")

    def test_http_scheme_preserved(self):
        url = "http://example.com/page"
        result = canonicalize_url(url)
        assert result.startswith("http://")
