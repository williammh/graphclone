# Prong 2C – forced dynamic parents (HTML analysis)
# ---------------------------------------------------------------------------

class TestForcedDynamicParents:
    """Parent paths flagged by HTML analysis force child slug collapsing."""

    def test_forced_parent_collapses_child(self):
        netloc = "www.forexfactory.com"
        forced = {(netloc, ())}
        url = f"https://{netloc}/somerandomuser"
        result = canonicalize_url(url, forced_dynamic_parents=forced)
        assert result == f"https://{netloc}/:slug"

    def test_forced_parent_respects_static(self):
        netloc = "www.forexfactory.com"
        forced = {(netloc, ())}
        static = {(netloc, ()): {"calendar", "forum", "news"}}
        url = f"https://{netloc}/calendar"
        result = canonicalize_url(url, observed_static_segments=static, forced_dynamic_parents=forced)
        assert result == f"https://{netloc}/calendar"

    def test_forced_parent_on_nested_path(self):
        netloc = "www.example.com"
        forced = {(netloc, ("blog",))}
        url = f"https://{netloc}/blog/some-article"
        result = canonicalize_url(url, forced_dynamic_parents=forced)
        assert result == f"https://{netloc}/blog/:slug"

    def test_forced_parent_nav_override(self):
        """Nav signals (Prong 2B) beat forced-dynamic (Prong 2C)."""
        netloc = "www.forexfactory.com"
        forced = {(netloc, ())}
        stats = {
            (netloc, "/calendar"): SegmentStats(nav_link_count=3, in_link_count=10),
        }
        url = f"https://{netloc}/calendar"
        result = canonicalize_url(url, segment_stats=stats, forced_dynamic_parents=forced)
        assert result == f"https://{netloc}/calendar"


