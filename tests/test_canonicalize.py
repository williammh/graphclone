# ZipRecruiter-style dynamic middle segments  (/{company}/jobs)
# ---------------------------------------------------------------------------

class TestZipRecruiterMiddleSegments:
    """ziprecruiter.com/walmart/jobs, ziprecruiter.com/costco/jobs, etc.

    The dynamic segment is in the middle of the URL, not at the end.
    """

    NETLOC = "www.ziprecruiter.com"

    def _simulate_companies(self, companies):
        """Simulate observing many /company/jobs links."""
        # The company segment sits at position 0, parent = ()
        obs = {(self.NETLOC, ()): set(companies)}
        locked = set(companies[: config.SLUG_CARDINALITY_THRESHOLD])
        static = {(self.NETLOC, ()): locked}
        return obs, static

    def test_early_companies_kept_literal(self):
        obs, static = self._simulate_companies(["walmart", "costco"])
        url = f"https://{self.NETLOC}/walmart/jobs"
        result = canonicalize_url(url, obs, static)
        assert result == f"https://{self.NETLOC}/walmart/jobs"

    def test_companies_collapse_after_threshold(self):
        threshold = config.SLUG_CARDINALITY_THRESHOLD
        companies = [f"company{i}" for i in range(threshold)]
        obs, static = self._simulate_companies(companies)
        url = f"https://{self.NETLOC}/newcompany/jobs"
        result = canonicalize_url(url, obs, static)
        # The middle segment collapses, "jobs" stays
        assert result == f"https://{self.NETLOC}/:slug/jobs"

    def test_static_company_kept_after_threshold(self):
        threshold = config.SLUG_CARDINALITY_THRESHOLD
        companies = [f"company{i}" for i in range(threshold)]
        obs, static = self._simulate_companies(companies)
        # company0 was locked in as static
        url = f"https://{self.NETLOC}/company0/jobs"
        result = canonicalize_url(url, obs, static)
        assert result == f"https://{self.NETLOC}/company0/jobs"

    def test_trailing_segment_stays_literal(self):
        """'jobs' should never collapse since it's the same static value."""
        threshold = config.SLUG_CARDINALITY_THRESHOLD
        companies = [f"company{i}" for i in range(threshold)]
        obs, static = self._simulate_companies(companies)

        # Also register "jobs" as always appearing under each company
        # (Every company page links to /company/jobs, so jobs cardinality = 1)
        for company in companies:
            key = (self.NETLOC, (company,))
            obs[key] = {"jobs"}
            static[key] = {"jobs"}

        url = f"https://{self.NETLOC}/newcompany/jobs"
        result = canonicalize_url(url, obs, static)
        assert "/jobs" in result

    def test_multiple_middle_and_end_dynamic(self):
        """e.g. /company/location/jobs where company and location are dynamic."""
        threshold = config.SLUG_CARDINALITY_THRESHOLD
        companies = [f"company{i}" for i in range(threshold)]
        cities = [f"city{i}" for i in range(threshold)]

        obs = {
            (self.NETLOC, ()): set(companies),
            (self.NETLOC, ("newcompany",)): set(cities),
        }
        static = {
            (self.NETLOC, ()): set(companies[:threshold]),
            (self.NETLOC, ("newcompany",)): set(cities[:threshold]),
        }

        url = f"https://{self.NETLOC}/newcompany/newcity/jobs"
        result = canonicalize_url(url, obs, static)
        assert result == f"https://{self.NETLOC}/:slug/:slug/jobs"


